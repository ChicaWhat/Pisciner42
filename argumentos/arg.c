#include <unistd.h>

void	ft_putchar(char *str)
{
	int n;
	n = 0;
	while(str[n] != '\0')
	{
		n++;
	}
}

int	main(int argc, char **argv)
{
	write(1, &argc, 1);
	write(1, argv[1], 50);
	return (0);
}
