#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	negat;

	p = 'P';
	negat = 'N';
	if (n >= 0)
	{
		write(1, &p, 1);
	}
	else
	{
		write(1, &negat, 1);
	}
}
