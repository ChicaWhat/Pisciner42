#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	aux;

	aux = *a;
	*a = *b;
	*b = aux;
}

int	main(void)
{
	int	a;
	int	b;

	a = '4';
	b = '2';
	ft_swap(&a, &b);

	write(1, &a, 1);
	write(1, "\n", 1);
	write(1, &b, 1);
}
